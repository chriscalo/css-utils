const css = require("tagged-template-noop");

module.exports = css`
/*
================================================================================
  OPACITY
================================================================================
*/

${
  Object.entries({
    "none": 0,
    "0": 0,
    "10": 0.10,
    "20": 0.20,
    "25": 0.25,
    "30": 0.30,
    "40": 0.40,
    "50": 0.50,
    "60": 0.60,
    "70": 0.70,
    "75": 0.75,
    "80": 0.80,
    "90": 0.90,
    "100": 1,
    "full": 1,
  }).map(([name, value]) => css`
    
    [opacity-${name}] {
      opacity: ${value};
    }
    
  `).join("\n")
}

`;
